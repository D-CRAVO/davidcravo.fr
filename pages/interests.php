                    <h1>La Marche Nordique</h1>
                    <h2>Quels sont les bienfaits de la marche nordique ?</h2>
                    <p>
                        Les abdominaux, les bras, les pectoraux, les épaules et le cou se trouvent sollicités au même titre que fessiers et cuisses. 
                        La marche nordique est une pratique très complète qui sollicite 80% des chaînes musculaires et permet de sculpter le corps en fonction de ses objectifs.
                    </p>
                    <h2>Quel type de chaussure pour la marche nordique ?</h2>
                    <p>
                        Dans ce cas une chaussure de running fera très bien l'affaire. 
                        En revanche en tout terrain ou en usage polyvalent (usage le plus courant en marche nordique) choisissez des crampons bien sculptés pour favoriser une "accroche" plus importante.
                    </p>
                    <h2>Quels muscles travaillent lors de la marche nordique ?</h2>
                    <p>
                        Grâce à l'utilisation des bâtons, le mouvement naturel de la marche est amplifié et fait travailler les muscles des épaules, des bras et les abdominaux autant que les muscles fessiers et les muscles des cuisses.
                    </p>
