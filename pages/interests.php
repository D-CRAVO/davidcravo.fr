                    <h2>Depuis quand est-ce que j'y joue ?</h2>
                    <p>
                        Je joue aux jeux vidéo depuis ma plus tendre enfance.                  J’ai débuté avec les jeux électroniques à écran monochrome et avec des piles bouton ou il s’agissait de ne pas percuter de voitures venant en sens contraire avec une définition somme toute très simpliste. J’ai enchainé avec une console de jeux à brancher sur l’antenne de la télévision ou il y avait 10 modes de jeux allant du casse-briques au ping-pong. J’ai franchi une étape en manipulant un Amstrad CPC 464 avec écran monochrome vert, où je devais d’abord passer des heures à taper du code en Basic. l’enregistrer sur cassette audio afin de jouer à des jeux plus élaborer. S’en sont suivis des années d’abstinence à la maison pour ne reprendre qu’avec la Playstation, première du nom, où j’ai pu découvrir le jeu vidéo contemporain. J’ai donc fait l’acquisition de toutes les PlayStation de chaque génération tout en montant des unités centrales à leds bleues pour d’autres jeux. Et aujourd’hui, même si le temps me manque je ne désespère pas de faire l’acquisition de la dernière PlayStation 5.
                    </p>
                    <h2>Quels sont mes jeux favoris ?</h2>
                    <p>
                        Je ne m’attarderai pas sur les jeux de première génération, pour n’aborder que ceux que j’ai connu à partir de la Playstation 1. Etant assez réfractaire aux jeux de tir à la première personne pour m’orienter vers Gran Turismo. Jeu de courses automobiles où les voitures sont toujours restées au top du réalisme de chaque modèle. Sans oublier les différents Need For Speed, Ridge Racer et Burnout. Dans le genre aventure, j’ai joué tous les Tomb Raider avec la fameuse Lara Croft, les Assassin’s Creed, et les Uncharted ou le scénario était beaucoup plus élaboré que dans les jeux de tir. J’ai assez peu pratiqué ces jeux qui ne m’apportaient que peu de plaisir en dehors des parties en lignes trop chronophages. Si dernièrement je n’en ai plus le temps, je ne désespère pas de taquiner à nouveau la manette avec les jeux de dernière génération.
                    </p>
                    <h2>Ma façon de jouer</h2>
                    <p>
                        J’ai toujours eu pour ambition de finir mes jeux à 100% avant d’en débuter d’autres. Ainsi je m’attèle à achever les quêtes secondaires et j’y arrive sur une bonne partie d’entre-elles. C’était même un point de grande satisfaction quand Sony Playstation a ajouté la fonctionnalité des trophées, similaire aux succès chez Microsoft Xbox, sur sa console de 3ème génération. Cela a eu pour effet de me faire refaire certains jeux, et m’acharner sur d’autres. J’ai ainsi pu mettre plus de deux ans à terminer entièrement certains jeux pour obtenir le saint-graal, le trophée de platine, sanctionnant la fin totale du jeu. De plus, cela a exacerbé mon esprit d’équipe, puisque certains trophées n’étant obtenu qu’en jouant en. Sur Burnout, par exemple, il m’est arrivé de jouer toute une nuit, avec d’autres joueurs du monde entier, où nous avions organisés des sessions pour atteindre notre but. Pour d’autres jeux, nous nous connections au site <a href="https://www.psthc.fr">https://www.psthc.fr</a> afin de s’organiser par jeux et par objectifs, au sein des forums. Cela m’a également permis de pratiquer les langues étrangères comme l’anglais, l’espagnol et le portugais, pour pouvoir communiquer en direct avec d’autres passionnés. 
